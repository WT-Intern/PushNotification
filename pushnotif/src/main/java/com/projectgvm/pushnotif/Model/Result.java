package com.projectgvm.pushnotif.Model;
public class Result {

	private String error;
	
	
	
	public Result() {
		super();
	}

	public Result(String error) {
		super();
		this.error = error;
	}

	public String getError() {
		return error;
	}

	public void setError(String error) {
		this.error = error;
	}
	
	
}
